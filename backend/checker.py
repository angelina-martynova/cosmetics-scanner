            r'inci[:\s]*',
            r'–∏–Ω–≥—Ä–µ–¥—ñ—î–Ω—Ç–∏[:\s]*',
            r'—Å–∫–ª–∞–¥[:\s]*',
            r'components[:\s]*',
            r'contents[:\s]*',
