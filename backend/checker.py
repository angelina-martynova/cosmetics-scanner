                    r'\n\s*\d+\.',      # –ù–æ–≤–∞—è —Å—Ç—Ä–æ–∫–∞ —Å –Ω–æ–º–µ—Ä–æ–º
                    r'\.\s*\n',         # –¢–æ—á–∫–∞ –∏ –Ω–æ–≤–∞—è —Å—Ç—Ä–æ–∫–∞
                    r'\n{2,}',          # –î–≤–µ –ø—É—Å—Ç—ã–µ —Å—Ç—Ä–æ–∫–∏
                    r'–ø—Ä–µ–¥–Ω–∞–∑–Ω–∞—á–µ–Ω–æ',   # –°–ª–µ–¥—É—é—â–∏–π —Ä–∞–∑–¥–µ–ª
